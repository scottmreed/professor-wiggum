"""Swappable storage interfaces for run-state persistence and artifact I/O.

These protocols define the surface area that must be implemented to swap the
default SQLite + local-filesystem backend for a cloud backend (e.g. Cloudflare
D1 + R2).  Current concrete implementations are:

    SQLiteRunStore   = RunStore      (mechanistic_agent.core.db)
    LocalArtifactStore               (below)

Dependency-inversion note
-------------------------
All coordinator and API code currently depends on RunStore directly.  The
Protocol classes here serve as the canonical interface contract: new cloud
adapters must implement them, and call-site type annotations should reference
these protocols rather than the concrete classes.  This refactor does NOT
change any runtime behaviour — it is purely a type-level and structural
improvement.
"""
from __future__ import annotations

from pathlib import Path
from typing import Any, Dict, List, Optional

try:
    from typing import Protocol, runtime_checkable
except ImportError:  # Python < 3.8
    from typing_extensions import Protocol, runtime_checkable  # type: ignore[assignment]


# ---------------------------------------------------------------------------
# RunStateStore — run lifecycle, events, step outputs, evaluations, etc.
# ---------------------------------------------------------------------------

@runtime_checkable
class RunStateStore(Protocol):
    """Protocol satisfied by RunStore (SQLite) and future cloud adapters."""

    # --- Run lifecycle ---
    def create_run(self, run_id: str, /, **kwargs: Any) -> None: ...
    def get_run_row(self, run_id: str) -> Optional[Dict[str, Any]]: ...
    def set_run_status(self, run_id: str, status: str) -> None: ...
    def delete_run(self, run_id: str) -> bool: ...
    def update_run_config(self, run_id: str, config: Dict[str, Any]) -> None: ...

    # --- Events ---
    def append_event(self, run_id: str, /, **kwargs: Any) -> None: ...
    def list_events(self, run_id: str, *, after_seq: int = 0, limit: int = 500) -> List[Dict[str, Any]]: ...

    # --- Step outputs ---
    def record_step_output(self, run_id: str, /, **kwargs: Any) -> None: ...
    def list_step_outputs(self, run_id: str) -> List[Dict[str, Any]]: ...

    # --- Trace records ---
    def add_trace_record(self, /, **kwargs: Any) -> str: ...
    def list_trace_records(self, /, **kwargs: Any) -> List[Dict[str, Any]]: ...
    def get_trace_record(self, trace_id: str) -> Optional[Dict[str, Any]]: ...
    def approve_trace_record(self, trace_id: str, /, **kwargs: Any) -> None: ...

    # --- Prompt / model versioning ---
    def upsert_prompt_versions(self, prompts: List[Dict[str, Any]]) -> Dict[str, str]: ...
    def upsert_model_version(self, /, **kwargs: Any) -> str: ...
    def resolve_run_step_prompt_id(self, /, **kwargs: Any) -> Optional[str]: ...
    def bind_run_step_prompt(self, /, **kwargs: Any) -> None: ...
    def list_run_step_prompts(self, run_id: str) -> List[Dict[str, Any]]: ...
    def list_prompt_versions(self, /, **kwargs: Any) -> List[Dict[str, Any]]: ...
    def get_prompt_version(self, prompt_version_id: str) -> Optional[Dict[str, Any]]: ...
    def get_model_version(self, model_version_id: str) -> Optional[Dict[str, Any]]: ...

    # --- Eval sets / runs ---
