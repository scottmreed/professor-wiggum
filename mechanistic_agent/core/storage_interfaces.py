# ---------------------------------------------------------------------------
# ArtifactStore — blob / trace file I/O
# ---------------------------------------------------------------------------

@runtime_checkable
class ArtifactStore(Protocol):
    """Protocol for reading and writing run artifacts (traces, evidence, etc.).

    The local implementation uses the filesystem under traces_root().
    A cloud implementation would use e.g. Cloudflare R2 or S3.
    """

    def write_artifact(self, relative_path: str, content: bytes) -> Path:
        """Write *content* to *relative_path* (relative to the artifact root).

        Returns the concrete Path (or a mock Path for remote stores).
        """
        ...

    def read_artifact(self, relative_path: str) -> Optional[bytes]:
        """Return the bytes at *relative_path*, or None if not found."""
        ...

    def artifact_path(self, relative_path: str) -> Path:
        """Return the absolute Path for *relative_path* (for callers that still
        need a local Path, e.g. to pass to subprocess tools).

        Remote implementations may materialise the file locally on demand.
        """
        ...


# ---------------------------------------------------------------------------
# Default (local) implementations
# ---------------------------------------------------------------------------

# SQLiteRunStore is the existing RunStore — no changes to that class needed.
from mechanistic_agent.core.db import RunStore as SQLiteRunStore  # noqa: E402


class LocalArtifactStore:
    """Filesystem-backed ArtifactStore using the configured traces root."""

    def __init__(self, base_dir: Path) -> None:
        from mechanistic_agent.prompt_assets import traces_root
        self._root: Path = traces_root(base_dir)

    def write_artifact(self, relative_path: str, content: bytes) -> Path:
        dest = self._root / relative_path
        dest.parent.mkdir(parents=True, exist_ok=True)
        dest.write_bytes(content)
        return dest

    def read_artifact(self, relative_path: str) -> Optional[bytes]:
        p = self._root / relative_path
        if not p.exists():
            return None
        return p.read_bytes()

    def artifact_path(self, relative_path: str) -> Path:
        return self._root / relative_path
